class CollectionCharacter {

    constructor(id, name, description, thumbnail, urls, comics, events, series, user_id) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.thumbnail = thumbnail;
        this.urls = urls;
        this.comics = comics;
        this.events = events;
        this.series = series;
        this.user_id = user_id;
    }

}